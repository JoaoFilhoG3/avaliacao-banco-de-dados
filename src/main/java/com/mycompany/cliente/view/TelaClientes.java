    public void preencheTabela(){
        ClienteDAO CDAO = new ClienteDAO();
        lClientes = CDAO.getAll();
        String colunas[] = new String[]{"C√≥d.","Nome","Endere√ßo","Email","CPF","Idade"};
        DefaultTableModel tbmClientes = new DefaultTableModel(colunas, 0);
        for (int i = 0; i < lClientes.size(); i++) {
            Cliente cli = lClientes.get(i);
            tbmClientes.addRow(new Object[]{
                cli.getCodCli(),
                cli.getNome(),
                cli.getEndereco(),
                cli.getEmail(),
                cli.getCpf(),
                cli.getIdade(),
            });
        }
        tblClientes.setModel(tbmClientes);
    }
